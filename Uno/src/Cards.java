
public class Cards
	{
		String Color;
		String Number;

		public Cards(String c, String n)
			{
				Color = c;
				Number = n;
			}

		public String getColor()
			{
				return Color;
			}

		public void setColor(String color)
			{
				Color = color;
			}

		public String getNumber()
			{
				return Number;
			}

		public void setNumber(String number)
			{
				Number = number;
			}
	}
